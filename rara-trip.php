<h1>Rara lake-raling festivals trek</h1>
<p class="rara">
Welcome to real opportunity for both a personal challenge and to support communities in need. This ‘Trek with a Purpose’ from Ghamgadi (Mugu District) via Rara Lake up to Raling Festivals in Humla is about being a part of positive change in one of the worlds remotest and ruggedly beautiful yet most impoverished regions in the ‘Hidden Himalayas’ of North West Nepal. There has been enough coverage of upper Humla but the communities of lower Humla are in the need & deep-end of the Himalayas that have suffered through a decade of a destructive insurgency need the support of a New Tourism For A New Century - a tourism that is a part of the solution and not a part of the problem for our world.
</p>
